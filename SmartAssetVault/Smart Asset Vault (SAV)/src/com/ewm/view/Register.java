    private boolean isValidEmail(String email) {
        // simple validation (good for coursework)
        return email != null && email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$");
    }

    private boolean isValidPhone(String phone) {
        // allow 7-15 digits
        return phone != null && phone.matches("^\\d{7,15}$");
    }

    private boolean isStrongPassword(String pass) {
        // at least 6 chars, contains letter + number
        if (pass == null) return false;
        if (pass.length() < 6) return false;
        boolean hasLetter = pass.matches(".*[A-Za-z].*");
        boolean hasDigit = pass.matches(".*\\d.*");
        return hasLetter && hasDigit;
    }

