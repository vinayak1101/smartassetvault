    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String fullName = jTextField4.getText().trim();
    String phone = jTextField2.getText().trim();
    String email = jTextField1.getText().trim();
    String pass = jTextField3.getText().trim();
    String gender   = getSelectedGender();
if (fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Full Name is required!");
            return;
        }
        if (phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone Number is required!");
            return;
        }
        if (!isValidPhone(phone)) {
            JOptionPane.showMessageDialog(this, "Phone must contain only digits (7-15 digits).");
            return;
        }
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required!");
            return;
        }
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, "Enter a valid email address!");
            return;
        }
        if (pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password is required!");
            return;
        }
        if (!isStrongPassword(pass)) {
            JOptionPane.showMessageDialog(this, "Password must be at least 6 chars and include letters + numbers.");
            return;
        }
        if (gender.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select gender!");
            return;
        }

try {
        UserModel user = new UserModel();
        user.setFullName(fullName);
        user.setPhone(phone);
        user.setEmail(email);
        user.setPassword(pass);
        user.setGender(gender);
        user.setRole("USER");

        boolean ok = registerController.register(user);

        if (!ok) {
            JOptionPane.showMessageDialog(this,
                    "Registration failed! Email may already exist.");
            return;
        }

        //  SUCCESS ONLY IF REGISTERED
        JOptionPane.showMessageDialog(this,
                "Registration Successful!\nNow login.");

        new Login().setVisible(true);
        this.dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,"Error: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

